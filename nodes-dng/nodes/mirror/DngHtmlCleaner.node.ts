import type {
  IExecuteFunctions,
  INodeType,
  INodeTypeDescription,
  INodeExecutionData,
} from 'n8n-workflow';
import { NodeConnectionType, NodeOperationError } from 'n8n-workflow';

export class DngHtmlCleaner implements INodeType {
  description: INodeTypeDescription = {
    displayName: "HTML Cleaner",
    name: "dngHtmlCleaner",
    group: ['transform'],
    version: 1,
    defaults: { name: "HTML Cleaner" },
    description: "Strip scripts, styles, and noise from HTML",
    inputs: [NodeConnectionType.Main],
    outputs: [NodeConnectionType.Main],
    properties: [
      { displayName: "DNG", name: "dngNotice", type: "notice", default: "DNG label: HTML Cleaner" }
    ],
  };

  async execute(this: IExecuteFunctions) {
    const items = this.getInputData();
    if (!items.length) {
      throw new NodeOperationError(
        this.getNode(),
        'DNG mirror nodes need an incoming main connection (Manual trigger or previous DNG node).',
      );
    }
        return await this.prepareOutputData([...items]);
  }
}

export default DngHtmlCleaner;

